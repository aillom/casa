function adapterPack(adapterName, title, skills) {
  return `${generatedHeader}# ${title}

This generated pack translates the C.A.S.A Core into a portable ${adapterName} reference.

## Skills

${skills
  .map((skill) => `## ${skill.name}\n\nSource: \`${skill.sourcePath}\`\n\n${skill.content}`)
  .join("\n\n")}
`
}

export function buildAdapterFiles() {
  const skills = listSkillFiles()
  const files = []

  for (const skill of skills) {
    files.push({
      targetPath: `.codex/skills/${skill.name}/SKILL.md`,
      sourcePaths: [skill.sourcePath],
      content: codexHeader + skill.content + "\n"
    })
  }

  files.push(
    {
      targetPath: ".cursor/rules/00-casa-context.mdc",
      sourcePaths: ["casa.manifest.yaml", "AGENTS.md"],
      content: cursorContextRule()
    },
    {
      targetPath: ".cursor/rules/01-casa-security.mdc",
      sourcePaths: ["casa.manifest.yaml", ".casa/kernel/policies/secure-coding.md"],
      content: cursorSecurityRule()
    },
    {
      targetPath: ".cursor/rules/02-casa-legacy.mdc",
      sourcePaths: ["casa.manifest.yaml", ".casa/capabilities/workflows/modernize-legacy-module.workflow.md"],
      content: cursorLegacyRule()
    },
    {
      targetPath: ".agents/casa-agent-guide.md",
      sourcePaths: ["AGENTS.md", "casa.manifest.yaml"],
      content: genericAgentGuide()
    },
    {
      targetPath: ".casa/generated/adapters/generic/AGENT-GUIDE.md",
      sourcePaths: ["AGENTS.md", "casa.manifest.yaml"],
      content: genericAgentGuide()
    },
    {
      targetPath: ".casa/generated/adapters/codex/SKILLS.md",
      sourcePaths: skills.map((skill) => skill.sourcePath),
      content: adapterPack("Codex", "Codex Skills Pack", skills)
    },
    {
      targetPath: ".casa/generated/adapters/claude/SKILLS.md",
      sourcePaths: skills.map((skill) => skill.sourcePath),
      content: adapterPack("Claude", "Claude Skills Pack", skills)
    },
    {
      targetPath: ".casa/generated/adapters/devin/KNOWLEDGE.md",
      sourcePaths: skills.map((skill) => skill.sourcePath),
      content: adapterPack("Devin", "Devin Knowledge Pack", skills)
    },
    {
      targetPath: ".casa/generated/adapters/antigravity/WORKFLOWS.md",
      sourcePaths: [
        ".casa/capabilities/workflows/implement-feature.workflow.md",
        ".casa/capabilities/workflows/modernize-legacy-module.workflow.md"
      ],
      content: `${generatedHeader}# Antigravity Workflow Pack

## Implement Feature

${readText(".casa/capabilities/workflows/implement-feature.workflow.md").trimEnd()}

## Modernize Legacy Module

${readText(".casa/capabilities/workflows/modernize-legacy-module.workflow.md").trimEnd()}
`
    },
    {
      targetPath: ".casa/generated/skills.index.md",
      sourcePaths: skills.map((skill) => skill.sourcePath),
      content: generatedSkillIndex(skills)
    }
  )

  const manifestItems = files.map((file) => ({
    targetPath: file.targetPath,
    sourcePaths: file.sourcePaths,
    sourceSha256: sha256(file.sourcePaths.map((sourcePath) => readText(sourcePath)).join("\n---\n")),
    targetSha256: sha256(file.content)
  }))

  files.push({
    targetPath: ".casa/generated/adapters.manifest.json",
    sourcePaths: files.flatMap((file) => file.sourcePaths),
    content: `${JSON.stringify({ generatedBy: "scripts/generate-adapters.mjs", files: manifestItems }, null, 2)}\n`
  })

  return files
}

export function writeAdapterFiles(files) {
  for (const file of files) {
    fs.mkdirSync(path.dirname(file.targetPath), { recursive: true })
    fs.writeFileSync(file.targetPath, file.content)
  }
}

export function findAdapterDrift(files) {
  return files.filter((file) => !fs.existsSync(file.targetPath) || readText(file.targetPath) !== file.content)
}
