import crypto from "node:crypto"
import fs from "node:fs"
import path from "node:path"

const skillRoot = ".casa/capabilities/skills"

const codexHeader = `<!--
Generated from .casa/capabilities/skills.
Do not edit this file directly.
Edit the C.A.S.A core skill and regenerate adapters.
-->

`

const generatedHeader = `<!--
Generated from C.A.S.A Core.
Do not edit this file directly.
Edit .casa files and regenerate adapters.
-->

`

function readText(filePath) {
  return fs.readFileSync(filePath, "utf8")
}

function sha256(value) {
  return crypto.createHash("sha256").update(value).digest("hex")
}

function listSkillFiles() {
  if (!fs.existsSync(skillRoot)) {
    return []
  }

  return fs
    .readdirSync(skillRoot, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .filter((entry) => fs.existsSync(path.join(skillRoot, entry.name, "SKILL.md")))
    .map((entry) => {
      const sourcePath = path.join(skillRoot, entry.name, "SKILL.md")
      return {
        name: entry.name,
        sourcePath,
        content: readText(sourcePath).trimEnd()
      }
    })
    .sort((a, b) => a.name.localeCompare(b.name))
}

function readManifestList(listName, indent = 0) {
  const manifest = readText("casa.manifest.yaml").split("\n")
  const key = `${" ".repeat(indent)}${listName}:`
  const start = manifest.findIndex((line) => line === key)

  if (start === -1) {
    return []
  }

  const itemPrefix = `${" ".repeat(indent + 2)}- `
  const items = []

  for (const line of manifest.slice(start + 1)) {
    if (line.startsWith(itemPrefix)) {
      items.push(line.slice(itemPrefix.length).replace(/^"|"$/g, ""))
      continue
    }

    if (line.trim() !== "" && !line.startsWith(" ".repeat(indent + 2))) {
      break
    }
  }

  return items
}

function readProtectedPaths() {
  const manifest = readText("casa.manifest.yaml").split("\n")
  const start = manifest.findIndex((line) => line === "  protected_paths:")

  if (start === -1) {
    return []
  }

  const items = []

  for (const line of manifest.slice(start + 1)) {
    if (line.startsWith("    - ")) {
      items.push(line.slice(6).replace(/^"|"$/g, ""))
      continue
    }

    if (line.trim() !== "" && !line.startsWith("    ")) {
      break
    }
  }

  return items
}

function bulletList(items) {
  return items.map((item) => `- ${item}`).join("\n")
}

function generatedSkillIndex(skills) {
  return `${generatedHeader}# Generated Skill Index

Source: \`.casa/capabilities/skills\`

${skills.map((skill) => `- ${skill.name}: ${skill.sourcePath}`).join("\n")}
`
}

function cursorContextRule() {
  const principles = readManifestList("principles")
  const protectedPaths = readProtectedPaths()

  return `---
description: C.A.S.A project context
alwaysApply: true
---

${generatedHeader}This repository follows C.A.S.A - Context, Architecture, Stack & Automation.

Core principles:
${bulletList(principles)}

Before changing files:
1. Read AGENTS.md.
2. Check casa.manifest.yaml.
3. Follow relevant .casa kernel standards and policies.
4. Do not edit generated adapter files directly.
5. Update docs, examples or specs when behavior changes.

Protected paths:
${bulletList(protectedPaths)}
`
}

function cursorSecurityRule() {
  const protectedPaths = readProtectedPaths()

  return `---
description: C.A.S.A security rules
globs:
  - "**/*.{ts,tsx,js,jsx,md,yml,yaml,json}"
alwaysApply: false
---

${generatedHeader}Follow C.A.S.A security policies in \`.casa/kernel/policies\`.

Never:
- hardcode secrets
- log tokens, passwords, cookies or sensitive data
- disable security checks without justification
- create public endpoints without explicit authorization decision
- edit production infrastructure without approval
- modify migrations without risk review

Always:
- validate external input
- consider authorization impact
- update security docs when policies change
- request security review for high-risk changes

Protected paths:
${bulletList(protectedPaths)}
`
}

function cursorLegacyRule() {
  const strategy = readManifestList("strategy", 2)

  return `---
description: C.A.S.A brownfield and legacy rules
globs:
  - "**/*"
alwaysApply: false
---

${generatedHeader}For legacy or unclear code:

1. Understand before changing.
2. Map dependencies.
3. Identify behavior to preserve.
4. Add characterization tests when possible.
5. Create seams before refactoring.
6. Prefer incremental modernization over rewrites.
7. Document risks and unknowns.

Modernization strategy:
${bulletList(strategy)}
`
}

